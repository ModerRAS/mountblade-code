        {
          new Tuple<TraitObject, int>(DefaultTraits.Honor, 30)
        });
        GainRenownAction.Apply(Hero.MainHero, 3f);
        GiveGoldAction.ApplyBetweenCharacters((Hero) null, Hero.MainHero, this.RewardGold);
        this.RelationshipChangeWithQuestGiver = 8;
        this.ChangeRelationWithRuralNotables(-2);
        this.CompleteQuestWithSuccess();
      }

      private void ChangeRelationWithRuralNotables(int value)
      {
        foreach (Settlement visitedVillages in this._visitedVillagesList)
        {
          foreach (Hero notable in (List<Hero>) visitedVillages.Notables)
          {
            if (notable.IsRuralNotable)
              ChangeRelationAction.ApplyPlayerRelation(notable, value);
          }
        }
      }

      protected override void OnTimedOut()
      {
        this.AddLog(this.QuestFailedWithTimeOutLogText);
        TraitLevelingHelper.OnIssueSolvedThroughQuest(this.QuestGiver, new Tuple<TraitObject, int>[1]
        {
          new Tuple<TraitObject, int>(DefaultTraits.Honor, -10)
        });
        this.RelationshipChangeWithQuestGiver = -5;
      }

      public override void OnFailed() => this.RelationshipChangeWithQuestGiver = -5;

      protected override void HourlyTick()
      {
      }

      protected override void RegisterEvents()
      {
        CampaignEvents.SettlementEntered.AddNonSerializedListener((object) this, new Action<MobileParty, Settlement, Hero>(this.OnSettlementEntered));
        CampaignEvents.HourlyTickPartyEvent.AddNonSerializedListener((object) this, new Action<MobileParty>(this.HourlyTickParty));
        CampaignEvents.WarDeclared.AddNonSerializedListener((object) this, new Action<IFaction, IFaction, DeclareWarAction.DeclareWarDetail>(this.OnWarDeclared));
        CampaignEvents.OnClanChangedKingdomEvent.AddNonSerializedListener((object) this, new Action<Clan, Kingdom, Kingdom, ChangeKingdomAction.ChangeKingdomActionDetail, bool>(this.OnClanChangedKingdom));
        CampaignEvents.MapEventStarted.AddNonSerializedListener((object) this, new Action<MapEvent, PartyBase, PartyBase>(this.OnMapEventStarted));
        CampaignEvents.GameMenuOpened.AddNonSerializedListener((object) this, new Action<MenuCallbackArgs>(this.OnGameMenuOpened));
        CampaignEvents.MapEventEnded.AddNonSerializedListener((object) this, new Action<MapEvent>(this.OnMapEventEnd));
        CampaignEvents.CanHeroBecomePrisonerEvent.AddNonSerializedListener((object) this, new ReferenceAction<Hero, bool>(this.OnCanHeroBecomePrisonerInfoIsRequested));
      }

      private void OnCanHeroBecomePrisonerInfoIsRequested(Hero hero, ref bool result)
      {
        if (hero != Hero.MainHero || !this._checkForEventEnd)
          return;
        result = false;
      }

      private void OnMapEventEnd(MapEvent obj)
      {
        if (!this._checkForEventEnd)
          return;
        if (PlayerEncounter.Battle.WinningSide == BattleSideEnum.None)
          this.QuestFailWithPlayerDefeatedAgainstLesserNobleParty();
        else if (PlayerEncounter.Battle.WinningSide == PlayerEncounter.Battle.PlayerSide)
          this.QuestSuccessWithPlayerDefeatedLesserNobleParty();
        else
          this.QuestFailWithPlayerDefeatedAgainstLesserNobleParty();
        if (this._lesserNobleParty.IsActive)
          DestroyPartyAction.Apply(PartyBase.MainParty, this._lesserNobleParty);
        this._checkForEventEnd = false;
      }

      private void OnGameMenuOpened(MenuCallbackArgs args)
      {
        if (!(args.MenuContext.GameMenu.StringId == "encounter") || PlayerEncounter.Battle == null || !PlayerEncounter.Battle.InvolvedParties.Contains<PartyBase>(this._lesserNobleParty.Party) || PlayerEncounter.Battle.State != MapEventState.Wait || MapEvent.PlayerMapEvent == null || !MapEvent.PlayerMapEvent.InvolvedParties.Contains<PartyBase>(this._lesserNobleParty.Party))
          return;
        foreach (PartyBase partyBase in MapEvent.PlayerMapEvent.InvolvedParties.ToList<PartyBase>())
        {
          if (partyBase.IsMobile && partyBase.MobileParty.IsMilitia)
            partyBase.MapEventSide = (MapEventSide) null;
        }
      }

      private void OnMapEventStarted(
        MapEvent mapEvent,
        PartyBase attackerParty,
        PartyBase defenderParty)
      {
        if (QuestHelper.CheckMinorMajorCoercion((QuestBase) this, mapEvent, attackerParty))
          QuestHelper.ApplyGenericMinorMajorCoercionConsequences((QuestBase) this, mapEvent);
        if (!this.IsOngoing || attackerParty != PartyBase.MainParty || defenderParty != this._lesserNobleParty.Party)
          return;
        this._checkForEventEnd = true;
      }

      private void HourlyTickParty(MobileParty mobileParty)
      {
        if (mobileParty != this._lesserNobleParty)
          return;
        if (this._lesserNobleParty.TargetSettlement == null)
        {
          if (this._goOutFromSettlementAfterHoursCounter > 0)
            --this._goOutFromSettlementAfterHoursCounter;
          if (this._goOutFromSettlementAfterHoursCounter == 0)
          {
            if (this._suitableVillagesToVisitList.Count == 0)
              this.CollectSuitableVillages();
            if (this._suitableVillagesToVisitList.Count > 0)
              SetPartyAiAction.GetActionForVisitingSettlement(this._lesserNobleParty, this._suitableVillagesToVisitList.OrderBy<Settlement, float>((Func<Settlement, float>) (x => x.Position2D.DistanceSquared(this._lesserNobleParty.Position2D))).First<Settlement>());
            else
              this._goOutFromSettlementAfterHoursCounter = 5;
          }
        }
        if (this._giveNotificationAfterHoursCounter > 0)
          --this._giveNotificationAfterHoursCounter;
        if (this._giveNotificationAfterHoursCounter != 0 || this._visitedVillagesList.Count <= 0)
          return;
        TextObject partyLocationLog = this.QuestLesserNoblePartyLocationLog;
        partyLocationLog.SetTextVariable("VILLAGE", this._visitedVillagesList[this._visitedVillagesList.Count - 1].EncyclopediaLinkWithName);
        MBInformationManager.AddQuickInformation(partyLocationLog);
        this.AddLog(partyLocationLog);
        foreach (Settlement visitedVillages in this._visitedVillagesList)
        {
          if (this.IsTracked((ITrackableCampaignObject) visitedVillages))
            this.RemoveTrackedObject((ITrackableCampaignObject) visitedVillages);
        }
        this.AddTrackedObject((ITrackableCampaignObject) this._visitedVillagesList[this._visitedVillagesList.Count - 1]);
        this._giveNotificationAfterHoursCounter = -1;
      }

      private void OnSettlementEntered(MobileParty party, Settlement settlement, Hero hero)
      {
        if (party != this._lesserNobleParty)
          return;
        this._visitedVillagesList.Add(settlement);
        this._suitableVillagesToVisitList.Remove(settlement);
        this._goOutFromSettlementAfterHoursCounter = 5;
        this._giveNotificationAfterHoursCounter = 9;
        if (80 > this._lesserNobleParty.MemberRoster.TotalManCount)
          this._lesserNobleParty.MemberRoster.AddToCounts((double) MBRandom.RandomFloat < 0.7 ? this._tier5Troop : this._tier6Troop, (double) MBRandom.RandomFloat < 0.5 ? 3 : 4);
        this._lesserNobleParty.ItemRoster.AddToCounts(DefaultItems.Grain, 5);
        this._discreteLog.UpdateCurrentProgress(this._discreteLog.CurrentProgress + 1);
        if (this._discreteLog.CurrentProgress != 20)
          return;
        this.OnTimedOut();
        this.CompleteQuestWithFail();
      }

      private void OnClanChangedKingdom(
        Clan clan,
        Kingdom oldKingdom,
        Kingdom newKingdom,
        ChangeKingdomAction.ChangeKingdomActionDetail detail,
        bool showNotification = true)
      {
        if (this.QuestGiver.MapFaction.IsAtWarWith(Hero.MainHero.MapFaction))
        {
          this.CompleteQuestWithCancel(this.QuestCanceledWarDeclared);
        }
        else
        {
          if (clan != this.QuestGiver.Clan)
            return;
          this.CompleteQuestWithCancel(this.GetQuestCanceledOnClanChangedKingdomLogText(oldKingdom));
        }
      }

      private void OnWarDeclared(
        IFaction faction1,
        IFaction faction2,
        DeclareWarAction.DeclareWarDetail detail)
      {
        QuestHelper.CheckWarDeclarationAndFailOrCancelTheQuest((QuestBase) this, faction1, faction2, detail, this.PlayerDeclaredWarQuestLogText, this.QuestCanceledWarDeclared);
      }

      protected override void OnFinalize()
      {
        if (!this._lesserNobleParty.IsActive)
          return;
        DestroyPartyAction.Apply((PartyBase) null, this._lesserNobleParty);
      }

      protected override void InitializeQuestOnGameLoad()
      {
        this._tier5Troop = CharacterHelper.GetTroopTree(this.QuestGiver.Culture.EliteBasicTroop, 5f, 5f).First<CharacterObject>();
        this._tier6Troop = CharacterHelper.GetTroopTree(this.QuestGiver.Culture.EliteBasicTroop, 6f, 6f).First<CharacterObject>();
        this.SetDialogs();
      }
    }

