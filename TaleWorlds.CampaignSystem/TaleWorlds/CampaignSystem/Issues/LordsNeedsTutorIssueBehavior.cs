      }

      public void OnHeroGainedSkill(Hero hero, SkillObject skill, int change = 1, bool shouldNotify = true)
      {
        if (hero != this._youngHero)
          return;
        this._startQuestLog.UpdateCurrentProgress(MBMath.ClampInt(this._youngHero.HeroDeveloper.GetTotalSkillPoints() - this._youngHeroBeginningSkillPoints, 0, 30));
        if (this._youngHero.HeroDeveloper.GetTotalSkillPoints() < this._targetSkillGain)
          return;
        this._questCompletedStartConversation = true;
      }

      private void OnPrisonerTaken(PartyBase capturer, Hero prisoner)
      {
        if (prisoner != Hero.MainHero || !this._youngHero.IsPlayerCompanion || MobileParty.MainParty.MapEventSide == null || !MobileParty.MainParty.MapEventSide.Parties.First<MapEventParty>((Func<MapEventParty, bool>) (x => x.Party == PartyBase.MainParty)).Troops.FindIndexOfCharacter(this._youngHero.CharacterObject).IsValid)
          return;
        this.AddLog(this.PupilEscapedLogText);
        EndCaptivityAction.ApplyByEscape(this._youngHero);
      }

      private void OnHeroKilled(
        Hero victim,
