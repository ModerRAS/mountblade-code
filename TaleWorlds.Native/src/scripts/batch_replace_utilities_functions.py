def replace_function_names(content):
    """替换函数名"""
    for old_name, new_name in FUNCTION_RENAME_MAP.items():
        # 替换函数定义
        content = re.sub(rf'\b{old_name}\b', new_name, content)
        # 替换函数调用
        content = re.sub(rf'\b{old_name}\b', new_name, content)
    return content

def main():
    if len(sys.argv) != 2:
        print("用法: python batch_replace_utilities_functions.py <文件路径>")
        sys.exit(1)
    
    file_path = sys.argv[1]
    
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # 替换函数名
        content = replace_function_names(content)
        
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content)
        
        print(f"成功替换 {file_path} 中的函数名")
        print(f"替换了 {len(FUNCTION_RENAME_MAP)} 个函数名")
        
    except Exception as e:
        print(f"处理文件时出错: {e}")
        sys.exit(1)

if __name__ == "__main__":
    main()